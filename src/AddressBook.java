public class AddressBook {
    String firstName;
    String lastName;
    String Address;
    String city;
    String state;
    int zip;
    String email;

    public AddressBook(String firstName,String lastName,String Address,String city,String state,int zip,String email){
        this.firstName=firstName;
        this.lastName=lastName;
        this.Address=Address;
        this.city=city;
        this.state=state;
        this.zip=zip;
        this.email=email;


    }

}
